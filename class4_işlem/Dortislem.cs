using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace class4_işlem
{
    internal class Dortislem
    {
        public double toplam(double a, double b)
        {
            return a + b;
        }
        public double cıkar(double a, double b)
        {
            return a - b;
        }
        public double carp(double a, double b)
        {
            return a * b;
        }
        public double böl(double a, double b)
        {
            return a / b;
        }
        public double mod(double a, double b)
        {
            return a % b;
        }
    }
}
