using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace büyük_olanı_bul
{
    internal class Buyukolanıbul
    {
        public int Bul(int a, int b)
        {
            int sonuc;
            if (a < b)
            {
                return sonuc = b;
            }
            else
            {
                return sonuc = a;
            }
        }
    }
